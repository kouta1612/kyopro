package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

var sc = bufio.NewScanner(os.Stdin)

func scanInt() int {
	sc.Scan()

	res, err := strconv.Atoi(sc.Text())
	if err != nil {
		panic(err)
	}

	return res
}

func main() {
	sc.Split(bufio.ScanWords)

	n, m := scanInt(), scanInt()
	fmt.Println(n - m)
}
